def set_functions():
    set_a = {1, 2, 3}
    set_b = {3, 4, 5}

    print("Set A:", set_a)
    print("Set B:", set_b)

    print("Union:", set_a | set_b)
    print("Intersection:", set_a & set_b)
    print("Difference:", set_a - set_b)
    print("Symmetric Difference:", set_a ^ set_b)

set_functions()